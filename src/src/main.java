    private void search_queryKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_search_queryKeyReleased
        //Search Query Text Field on_change event
        String search = search_query.getText();
        String type = type_combo.getSelectedItem().toString();
        String ci = CI_combo.getSelectedItem().toString();
        if ((search.length() > 3 && type.toLowerCase().equals("name")) || type.toLowerCase().equals("id")){
            populate_list(type.toLowerCase(), search, ci);
            id_field.setText("");
            name_field.setText("");
            phone_field.setText("");
            ci_field.setText("");
        }
    }//GEN-LAST:event_search_queryKeyReleased

