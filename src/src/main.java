    List<lib.Student> students;
    
    //method to populate search result in the list
    private void populate_list(String type, String search, String ci){
        students = Lib.Get_Student(type, search, ci);
        search_list.setModel(new javax.swing.AbstractListModel<String>(){
            @Override
            public int getSize() { return students.size(); }

            @Override
            public String getElementAt(int index) {
                return students.get(index).student_name;
            }
        });
    }
    
    //method to populate fee table
    private void populate_fee(int id){
        List<lib.Payment> payments = Lib.Get_Payment(id);
        payment_table.setModel(new javax.swing.table.AbstractTableModel() {
            @Override
            public int getRowCount() {
                return payments.size();
            }

            @Override
            public int getColumnCount() {
                return 2;
            }

            @Override
            public Object getValueAt(int rowIndex, int columnIndex) {
                if (columnIndex == 0){
                    return new SimpleDateFormat("dd-mm-yyyy").format(payments.get(rowIndex).date);
                } else {
                    return "" + payments.get(rowIndex).amount;
                }
            }
            
            @Override
            public String getColumnName(int index){
                if (index == 0) return "Date";
                else return "Amount";
            }
            
        });
        
    }
    
